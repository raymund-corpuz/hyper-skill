package org.hyperskill.car;

public class Car {
    String brand;
    String model;
    double price;
    boolean isAvailable;

    public Car(String brand, String model, double price, boolean isAvailable) {
        this.brand = brand;
        this.model = model;
        this.price = price;
        this.isAvailable = isAvailable;
    }
    
}
