public class S3_DisplayProgrammingLanguages {
    public static void main(String[] args) {
        String[] programmingLanguages = {"Java", "C#", "C++", "Swift", "Python"};

        System.out.println("Sample 3: Display Programming Languages");
        System.out.println();

        for (String program : programmingLanguages) {
            System.out.println(program);
        }
        System.out.println();
        System.out.println("End Of Program...");
    }
}
